package ch11;

public class Selection {
//뽑기 문제
// 뽑기에 들어갈 만한것 몇가지 만들기
//현금 넣으면 물건 출력
	String name;
	
}

class Bear extends Selection{
	public Bear() {
		name = "곰인형";
		
	} 
	
}
class Gun extends Selection{
	public Gun() {
	name = "총";

	}
}
class Car extends Selection{
	public Car() {
		name = "자동차";
		
	}
	
}

